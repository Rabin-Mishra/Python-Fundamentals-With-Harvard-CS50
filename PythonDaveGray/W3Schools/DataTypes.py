# built in data types
# setting the datatype
# string
a = 'Hello World'
print(a)
print(type(a))
print('')
# integer
b = 20
print(b)
print(type(b))
print('')
# float
c = 20.5
print(c)
print(type(c))
print('')
# complex
d = 1j
print(d)
print(type(d))
print('')
# list
e = ['apple', 'banana', 'orange']
print(e)
print(type(e))
print('')
# tuple
f = ('apple', 'banana', 'orange')
print(f)
print(type(f))
print('')
# range
g = range(6)
print(g)
print(type(g))
print('')
# dictionary
h = {'name': 'John', 'age': 30}
print(h)
print(type(h))
print('')
# set
i = {'apple', 'banana', 'cherry'}
print(i)
print(type(i))
print('')
# frozenset
j = frozenset({'apple', 'banana', 'orange'})
print(j)
print(type(j))
print('')
# boolean
k = True
print(k)
print(type(k))
print('')
# bytes
l = b'Hello World'
print(l)
print(type(l))
print('')
# bytearray
m = bytearray(5)
print(m)
print(type(m))
print('')
# memorview
n = memoryview(bytes(5))
print(m)
print(type(m))
print('')
# none type
o = None
print(o)
print(type(o))
print('')
